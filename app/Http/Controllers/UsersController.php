    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
        $request->validate([
            'name' => ['required', 'string', 'max:255'],
            'email' => ['required', 'string', 'email', 'max:255', 'unique:users,email,'.auth()->id()],
            'password' => ['sometimes','nullable', 'string', 'min:8','confirmed'],
        ]);

        $user = auth()->user();
        $input = $request->except('password', 'psssword_confirmation');

        // Case When password is blank
        if (! $request->filled('password')) {
            $user->fill($input)->save();
            return back()->with('success_message', 'Profile Updated');
        }

        // Case when password is not blank
        $user->password = Hash::make($request->password);
        $user->fill($input)->save();
        return back()->with('success_message', 'Profile & Password Updated');
    }

